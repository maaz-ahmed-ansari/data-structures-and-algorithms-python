# List have indexes
# Linked List does not have indexes

# List is a contiguous space in memory i.e. all elements are next to each other
# Whereas in Linked List, all of nodes are spread all over the place

# With linked list, have a variable called "head" which points to first node in the list
# And a variable called "tail" which points to last item
# Note: if LL has only one node then both head and tail points to the node itself

# And each node points to the next node and last node points to "None"
# Each element/node is spread out in the memory, 
# and since each node points to next so they can be accessed 
# as oppossed to being contiguous space 
